if __name__ == '__main__':
    # Get RPC URL and private key
    rpc_url = os.getenv('RPC_URL') or (sys.argv[1] if len(sys.argv) > 1 else None)
    private_key = os.getenv('PRIVATE_KEY') or (sys.argv[2] if len(sys.argv) > 2 else None)
    chain_id = int(sys.argv[3]) if len(sys.argv) > 3 else 1
    
    # Deploy
    vault_address = deploy_vault(rpc_url, private_key, chain_id)
    
    if vault_address:
        show_next_steps(vault_address)
